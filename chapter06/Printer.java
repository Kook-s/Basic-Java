package chapter06;

public class Printer {

	void println(int num) {
		System.out.println(num);
	}

	void println(boolean num) {
		System.out.println(num);
	}

	void println(double num) {
		System.out.println(num);
	}

	void println(String num) {
		System.out.println(num);
	}

}
